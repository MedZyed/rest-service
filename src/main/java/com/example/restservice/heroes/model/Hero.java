package com.example.restservice.heroes.model;

import java.util.Objects;

public class Hero {

	private long id;
	private String name;

	public Hero(int id, String name) {
		this.id = id;
		this.name = name;
	}

	public Hero() {
		super();
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public boolean equals(Object o) {
		if (this == o)
			return true;
		if (o == null || getClass() != o.getClass())
			return false;
		Hero hero = (Hero) o;
		return id == hero.id && name.equals(hero.name);
	}

	@Override
	public int hashCode() {
		return Objects.hash(id, name);
	}

	@Override
	public String toString() {
		return "Hero{" + "id=" + id + ", name='" + name + '\'' + '}';
	}
}
